import os
import shutil
import subprocess
import requests
from pathlib import Path
from dotenv import load_dotenv

# Load environment variables from the .env file
load_dotenv()

# Securely fetch the token from the environment
GITHUB_TOKEN = os.getenv("GITHUB_TOKEN")

# --- Configuration ---
DOWNLOAD_DIR = "../codebases/svelte"
REPO_COUNT = 100

def get_top_svelte_repos():
    print(f"Fetching top {REPO_COUNT} Svelte repositories from GitHub...")
    headers = {"Accept": "application/vnd.github.v3+json"}
    if GITHUB_TOKEN:
        headers["Authorization"] = f"token {GITHUB_TOKEN}"
        
    # Queries repos tagged with 'svelte', sorted by stars
    url = f"https://api.github.com/search/repositories?q=topic:svelte&sort=stars&order=desc&per_page={REPO_COUNT}"
    response = requests.get(url, headers=headers)
    response.raise_for_status()
    return response.json()["items"]

def remove_readonly(func, path, excinfo):
    """Helper to remove read-only files generated by Git on Windows."""
    import stat
    os.chmod(path, stat.S_IWRITE)
    func(path)

def main():
    Path(DOWNLOAD_DIR).mkdir(exist_ok=True)
    
    try:
        repos = get_top_svelte_repos()
    except Exception as e:
        print(f"Failed to fetch repositories: {e}")
        return
    
    for i, repo in enumerate(repos, 1):
        repo_name = repo["name"]
        clone_url = repo["clone_url"]
        print(f"\n[{i}/{REPO_COUNT}] Processing '{repo_name}'...")
        
        repo_dir = Path(DOWNLOAD_DIR) / f"temp_{repo_name}"
        output_dir = Path(DOWNLOAD_DIR) / repo_name
        
        # Step 1: Shallow clone (--depth 1) to save time and bandwidth
        try:
            subprocess.run(["git", "clone", "--depth", "1", clone_url, str(repo_dir)], 
                           check=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        except subprocess.CalledProcessError:
            print(f"  -> Failed to clone {repo_name}. Skipping.")
            continue

        # Step 2: Find and copy .svelte files while maintaining folder structure
        svelte_count = 0
        for root, _, files in os.walk(repo_dir):
            # Skip the .git folder
            if ".git" in root:
                continue
                
            for file in files:
                # Target .svelte components
                if file.endswith(".svelte"):
                    source_path = Path(root) / file
                    # Calculate relative path to maintain directory structure
                    relative_path = source_path.relative_to(repo_dir)
                    dest_path = output_dir / relative_path
                    
                    # Create parent directories if they don't exist
                    dest_path.parent.mkdir(parents=True, exist_ok=True)
                    
                    try:
                        shutil.copy2(source_path, dest_path)
                        svelte_count += 1
                    except Exception as e:
                        print(f"  -> Error copying {file}: {e}")
        
        print(f"  -> Extracted {svelte_count} .svelte files.")
        
        # Step 3: Clean up the cloned repository to save disk space
        if repo_dir.exists():
            shutil.rmtree(repo_dir, onerror=remove_readonly)

    print(f"\nExtraction complete! All .svelte files are saved in the '{DOWNLOAD_DIR}' directory.")

if __name__ == "__main__":
    main()